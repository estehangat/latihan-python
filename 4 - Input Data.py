# User Input

# Data pasti string
data = input("Masukkan : ") # Input : 21
print("Karakter", data, "merupakan tipe data", type(data)) # Output : Karakter 21 merupakan tipe data <class 'str'>

# Change to int and float sama saja
data_int = int(input("Masukkan : ")) # Input : 21
print("Karakter", data_int, "merupakan tipe data", type(data_int)) # Output : Karakter 21 merupakan tipe data <class 'int'>

# Change to boolean
data_bool = bool(int(input("Masukkan : "))) # Input : 21
print("Karakter", data_bool, "merupakan tipe data", type(data_bool)) # Output : Karakter True merupakan tipe data <class 'bool'>
