# Penyingkatan Operasi

# Pertambahan
a = 9
a += 7
print("Hasil pertambahan", a)

# Pengurangan
a -= 1
print("Hasil pengurangan", a)

# Perkalian
a *= 4
print("Hasil Perkalian", a)

# Pembagian
a /= 3
print("Hasil Pembagian", int(a))

# Pembagian Sisa
a %= 6
print("Hasil sisanya", int(a))

# Pembagian pembulatan ke bawah
a //= 7
print("Hasil pembulatan ke bawah", int(a))

# Eksponen
a **= 0
print("Hasil eksponen", int(a))

# OR
q = True
q |= False
print("Maka akan menjadi", q)

# AND
q = True
q &= False
print("Maka akan menjadi", q)

# XOR
q = True
q ^= True
print("Maka akan menjadi", q)