# Operasi
# Prioritas seperti matematika
# Penamaan bisa menggunakan underscore untuk memudahkan melihat angka

''' 
    1. Kurung ()
    2. Eksponen **
    3. Perkalian, Pembagian *, /, //, % 
    4. Pertambahan, Pengurangan +, -
    
'''

a = 21
b = 9

# Pertambahan
print("Pertambahan", a+b)

# Pengurangan
print("Pengurangan", a-b)

# Perkalian
print("Perkalian", a*b)

# Eksponen (Pangkat)
print("Eksponen", a**b)

# Pembagian
print("Pembagian", a/b)

# Pembagian (Dibulatkan ke bawah)
print("Pembagian ke bawah", a//b)

# Pembagian (Sisa)
print("Pembagian sisa", a%b)

# Assignment indirect
p = a
print(p)
