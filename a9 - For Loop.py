# For Loop (Perulangan)

# Loop List
angka = [2,3,5,7,11]
for i in angka:
    print(f"Number {i}")
    # Output :
    # Number 2
    # Number 3
    # Number 5
    # Number 7
    # Number 11

# Range
for i in range(5):
    print(i)
    # Output :
    # Number 0-4

for i in range(9,22):
    print(i)
    # Output :
    # Number 9-21

# Loop String
for i in "Maeng":
    print(i)