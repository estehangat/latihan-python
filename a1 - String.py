# String

# Single Quote
user = 'Hello World!'
print(user)

# Double Quote
user = "Hello World!"
print(user)

# Quotes
print('"Hello World!"')
print("'Hello World!'")
print("It's 9 o'clock!")

# Backslash
print('It\'s 9 o\'clock!')
print("C:\\User\\asus\\Program Files")

# Tab
print("Purwokerto \tJakarta")

# Backspace
print("JKT \b48")

# Newline
print("Hello \nWorld!")
print("Hello \rWorld!")

# Raw String
print(r'D:\User\asus\Coding')

# Multiline String
print("""
Name : Bill Gates
Company : Microsoft
""")

# Multiline String and RAW
print(r"""
Name : Steve Jobs
Company : Microsoft
Website : www.microsoft.com/
""")
